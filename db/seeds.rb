Page.find_or_create_by!(title: "首页") do |p|
  p.body = <<~BODY
    欢迎来到维基！

    这是一个简单的知识共建平台。你可以在这里记录想法、整理知识、互相链接页面。

    使用 [[页面标题]] 语法链接到其他页面。例如：

    - [[使用指南]] — 了解如何编写页面
    - [[Markdown 格式]] — 文字排版参考
    - [[关于本站]] — 维基的背景与初衷

    点击红色虚线链接，可以直接创建一个新页面。
